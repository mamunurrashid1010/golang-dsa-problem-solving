package main

import "fmt"

func mergeSort(arr []int) []int {
	if len(arr) <= 1 {
		return arr
	}

	mid := len(arr) / 2

	left := mergeSort(arr[:mid])
	right := mergeSort(arr[mid:])

	return merge(left, right)
}

func merge(left, right []int) []int {
	result := []int{}
	i, j := 0, 0

	for i < len(left) && j < len(right) {
		if left[i] < right[j] {
			result = append(result, left[i])
			i++
		} else {
			result = append(result, right[j])
			j++
		}
	}

	result = append(result, left[i:]...)
	result = append(result, right[j:]...)

	return result
}

func main() {
	// slice declare
	arr := []int{10, 9, 8, 7, 6, 5, 4}

	// before merge
	fmt.Println("Before merge: ", arr)

	// sort array
	sorted_array := mergeSort(arr)

	// after merge
	fmt.Println("After Merge: ", sorted_array)
}
